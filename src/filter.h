  static const float __not_in_flash_func(jnr_maf1)(const float v)
  {
    static float element[JNR_FILTER_LENGTH] = {0.0f};
    static uint8_t p = 0;
    static float sum = 0.0f;
    sum = sum - element[p] + v;
    element[p++] = v;
    p &= (JNR_FILTER_LENGTH-1);
    return sum / ((float)JNR_FILTER_LENGTH);
  }

