/* --------------------------------------------------------------------------
 * Radix-2 butterfly
 * tw advances by fstride each iteration (uses *tw pointer walk).
 * -------------------------------------------------------------------------- */
static void kf_bfly2(kiss_fft_cpx* Fout, int fstride,
  const fft2500_state_t* st, int m)
{
  kiss_fft_cpx* Fout2 = Fout + m;
  const kiss_fft_cpx* tw = st->twiddles;
  do
  {
    const float ar = Fout->r;
    const float ai = Fout->i;
    const float br = Fout2->r;
    const float bi = Fout2->i;
    const float wr = tw->r;
    const float wi = tw->i;
    const float tr = br * wr - bi * wi;
    const float ti = br * wi + bi * wr;
    Fout2->r = ar - tr;
    Fout2->i = ai - ti;
    Fout->r = ar + tr;
    Fout->i = ai + ti;
    tw += fstride;
    ++Fout2; ++Fout;
  } while (--m);
}

/* --------------------------------------------------------------------------
 * Radix-5 butterfly
 * Twiddles accessed by explicit index from fixed base - tw must NOT advance.
 * ya = W_N^(N/5), yb = W_N^(2N/5) read from twiddle table.
 * -------------------------------------------------------------------------- */
static void kf_bfly5(kiss_fft_cpx* Fout, int fstride,
  const fft2500_state_t* st, int m)
{
  const kiss_fft_cpx* tw = st->twiddles;   /* fixed base, never moved */
  const kiss_fft_cpx ya = tw[fstride * m];
  const kiss_fft_cpx yb = tw[fstride * 2 * m];

  kiss_fft_cpx* Fout0 = Fout;
  kiss_fft_cpx* Fout1 = Fout + m;
  kiss_fft_cpx* Fout2 = Fout + 2 * m;
  kiss_fft_cpx* Fout3 = Fout + 3 * m;
  kiss_fft_cpx* Fout4 = Fout + 4 * m;

  for (int u = 0; u < m; ++u)
  {
    kiss_fft_cpx scratch[13];
    const float F0r = Fout0->r;
    const float F0i = Fout0->i;

    /* Apply twiddle factors W^(k*u) for k=1..4 */
    const kiss_fft_cpx* tw1 = &tw[u * fstride];
    const kiss_fft_cpx* tw2 = &tw[2 * u * fstride];
    const kiss_fft_cpx* tw3 = &tw[3 * u * fstride];
    const kiss_fft_cpx* tw4 = &tw[4 * u * fstride];

    scratch[1].r = Fout1->r * tw1->r - Fout1->i * tw1->i;
    scratch[1].i = Fout1->r * tw1->i + Fout1->i * tw1->r;
    scratch[2].r = Fout2->r * tw2->r - Fout2->i * tw2->i;
    scratch[2].i = Fout2->r * tw2->i + Fout2->i * tw2->r;
    scratch[3].r = Fout3->r * tw3->r - Fout3->i * tw3->i;
    scratch[3].i = Fout3->r * tw3->i + Fout3->i * tw3->r;
    scratch[4].r = Fout4->r * tw4->r - Fout4->i * tw4->i;
    scratch[4].i = Fout4->r * tw4->i + Fout4->i * tw4->r;

    scratch[7].r = scratch[1].r + scratch[4].r;
    scratch[7].i = scratch[1].i + scratch[4].i;
    scratch[10].r = scratch[1].r - scratch[4].r;
    scratch[10].i = scratch[1].i - scratch[4].i;
    scratch[8].r = scratch[2].r + scratch[3].r;
    scratch[8].i = scratch[2].i + scratch[3].i;
    scratch[9].r = scratch[2].r - scratch[3].r;
    scratch[9].i = scratch[2].i - scratch[3].i;

    Fout0->r = F0r + scratch[7].r + scratch[8].r;
    Fout0->i = F0i + scratch[7].i + scratch[8].i;

    scratch[5].r = F0r + scratch[7].r * ya.r + scratch[8].r * yb.r;
    scratch[5].i = F0i + scratch[7].i * ya.r + scratch[8].i * yb.r;
    scratch[6].r = scratch[10].i * ya.i + scratch[9].i * yb.i;
    scratch[6].i = -scratch[10].r * ya.i - scratch[9].r * yb.i;

    /* Note: Fout1 = scratch[5] - scratch[6]  (NOT +)  */
    Fout1->r = scratch[5].r - scratch[6].r;
    Fout1->i = scratch[5].i - scratch[6].i;
    Fout4->r = scratch[5].r + scratch[6].r;
    Fout4->i = scratch[5].i + scratch[6].i;

    scratch[11].r = F0r + scratch[7].r * yb.r + scratch[8].r * ya.r;
    scratch[11].i = F0i + scratch[7].i * yb.r + scratch[8].i * ya.r;
    scratch[12].r = -scratch[10].i * yb.i + scratch[9].i * ya.i;
    scratch[12].i = scratch[10].r * yb.i - scratch[9].r * ya.i;

    Fout2->r = scratch[11].r + scratch[12].r;
    Fout2->i = scratch[11].i + scratch[12].i;
    Fout3->r = scratch[11].r - scratch[12].r;
    Fout3->i = scratch[11].i - scratch[12].i;

    ++Fout0; ++Fout1; ++Fout2; ++Fout3; ++Fout4;
  }
}

/* --------------------------------------------------------------------------
 * Recursive FFT core  (from KissFFT kf_work)
 * OUT-OF-PLACE DIT: reads f[] strided, writes Fout[] contiguous.
 * -------------------------------------------------------------------------- */
static void kf_work(kiss_fft_cpx* Fout, const kiss_fft_cpx* f,
  int fstride, int in_stride,
  const int* factors, const fft2500_state_t* st)
{
  kiss_fft_cpx* Fout_beg = Fout;
  const int p = *factors++;
  const int m = *factors++;
  const kiss_fft_cpx* Fout_end = Fout + p * m;

  if (m == 1)
  {
    do { *Fout = *f; f += fstride * in_stride; } while (++Fout != Fout_end);
  }
  else
  {
    kiss_fft_cpx* f2 = Fout;
    do
    {
      kf_work(f2, f, fstride * p, in_stride, factors, st);
      f += fstride * in_stride;
      f2 += m;
    } while (f2 != Fout_end);
  }
  Fout = Fout_beg;
  switch (p)
  {
  case 2: kf_bfly2(Fout, fstride, st, m); break;
  case 5: kf_bfly5(Fout, fstride, st, m); break;
  }
}

/* --------------------------------------------------------------------------
 * Real-input FFT of N=2500  (public)
 *
 * in[0..2499]  : 2500 real samples
 * out[0..1250] : 1251 complex bins DC..Nyquist
 * -------------------------------------------------------------------------- */
static void fft2500_real(const float* in, kiss_fft_cpx* out)
{
  /* Complex FFT (real packed as interleaved complex) */
  kf_work(g_tmpbuf, (const kiss_fft_cpx*)in, 1, 1, k_st.factors, &k_st);

  /* ---- DC and Nyquist ---- */
  const float t0r = g_tmpbuf[0].r;
  const float t0i = g_tmpbuf[0].i;

  out[0].r = t0r + t0i;
  out[0].i = 0.0f;

  out[FFT2500_NCPX].r = t0r - t0i;
  out[FFT2500_NCPX].i = 0.0f;

  /* ---- Main recombination ---- */
  const int ncpx = FFT2500_NCPX;

  for (int k = 1; k <= ncpx / 2; ++k)
  {
    /* Load inputs */
    const kiss_fft_cpx* fk = &g_tmpbuf[k];
    const kiss_fft_cpx* fnk = &g_tmpbuf[ncpx - k];
    const float fpk_r = fk->r;
    const float fpk_i = fk->i;
    const float fpnk_r = fnk->r;
    const float fpnk_i = -fnk->i;   /* conjugate */

    /* f1k = fpk + fpnk */
    const float f1r = fpk_r + fpnk_r;
    const float f1i = fpk_i + fpnk_i;

    /* f2k = fpk - fpnk */
    const float f2r = fpk_r - fpnk_r;
    const float f2i = fpk_i - fpnk_i;

    /* Twiddle */
    const float wr = k_super_twiddles[k - 1].r;
    const float wi = k_super_twiddles[k - 1].i;

    /* tmp = f2k * tw */
    const float tr = f2r * wr - f2i * wi;
    const float ti = f2r * wi + f2i * wr;

    /* Output */
    kiss_fft_cpx* ok = &out[k];
    kiss_fft_cpx* onk = &out[ncpx - k];
    ok->r = 0.5f * (f1r + tr);
    ok->i = 0.5f * (f1i + ti);
    onk->r = 0.5f * (f1r - tr);
    onk->i = -0.5f * (f1i - ti);
  }
}

/* --------------------------------------------------------------------------
 * ft8_codec.h wrapper
 * Entry: re[0..2499]=real samples, im[]=0
 * Exit:  re[k],im[k]=spectrum bins k=0..1250; zeros above
 * -------------------------------------------------------------------------- */
static void ft8_fft_forward(float* re, float* im)
{
  static kiss_fft_cpx out[FFT2500_NBINS];
  fft2500_real(re, out);
  for (int k = 0; k < FFT2500_NBINS; ++k) { re[k] = out[k].r; im[k] = out[k].i; }
  for (int k = FFT2500_NBINS; k < FFT2500_NFFT; ++k) { re[k] = 0.0f; im[k] = 0.0f; }
}

